!=============================================================================================================================!
!                                                                                                                             !
!                           This subroutine copies file_name to
!                           file_name_new writing command to cmd
!                           !
!                                                                                                                             !
!=============================================================================================================================! 
     subroutine copy_file (file_name, file_name_new)
     use ifport 
     implicit none
!=============================================================================================================================
! D e c l a r a t i o n s
!=============================================================================================================================
     character(len=*),intent(IN) :: file_name_new,file_name
!----------------------------------------------------------------------------------------------------------------------------- 
     logical                     :: logical_result
!=============================================================================================================================
! S t a t e m e n t s
!============================================================================================================================= 

     logical_result = systemqq('copy "'//trim(file_name) //'"
"'//trim(file_name_new)//'"')

!==============================================================================================================================
     end subroutine copy_file 
